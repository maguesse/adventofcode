import main


def solve(data):
    obj = main.Day10(data)
    return obj.solve_part1()

def test_field1():
    data = ['.#..#',
            '.....',
            '#####',
            '....#',
            '...##']
    assert ((3,4), 8) == solve(data)

def test_field2():
    data = ['......#.#.', '#..#.#....',
            '..#######.',
            '.#.#.###..',
            '.#..#.....',
            '..#....#.#',
            '#..#....#.',
            '.##.#..###',
            '##...#..#.',
            '.#....####',]
    assert ((5,8), 33) == solve(data)


